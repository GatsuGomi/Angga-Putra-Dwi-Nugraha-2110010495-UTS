    private void TBALAMATMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_TBALAMATMouseClicked
        TableModel dt = TBALAMAT.getModel();
        int list = TBALAMAT.getSelectedRow();
        
        TFID.setEditable(false);

        TFID.setText(dt.getValueAt(list, 0).toString());
        TFNAMA.setText(dt.getValueAt(list, 1).toString());
        CBJENKEL.setSelectedItem(dt.getValueAt(list,2));
        TFNO.setText(dt.getValueAt(list, 3).toString());
        TAALAMAT.setText(dt.getValueAt(list, 4).toString());
    }//GEN-LAST:event_TBALAMATMouseClicked

    private void BTUpdateActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_BTUpdateActionPerformed
       String Id = TFID.getText().toString();
        String Nama = TFNAMA.getText().toString();
        String JK = CBJENKEL.getSelectedItem().toString();
        String Telp = TFNO.getText().toString();
        String Alamat = TAALAMAT.getText().toString();
        
        int data = 0;
        for (BkAlamat bk:arraybuku){
            if(bk.getId().equals(Id)){
                data = arraybuku.indexOf(bk);
                        break;
            }
        }
        arraybuku.set(data,new BkAlamat(Id,Nama,JK,Telp,Alamat));
         JOptionPane.showMessageDialog(null, "Data Berhasil Diupdate");
        reset();
    }//GEN-LAST:event_BTUpdateActionPerformed

    private void BTHAPUSActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_BTHAPUSActionPerformed
            int i  = TBALAMAT.getSelectedRow();
            DefaultTableModel model  = (DefaultTableModel) TBALAMAT.getModel();
            model.removeRow(i);
            arraybuku.remove(i);
            JOptionPane.showMessageDialog(null, "Data Berhasil dihapus");
            reset();
    }//GEN-LAST:event_BTHAPUSActionPerformed

