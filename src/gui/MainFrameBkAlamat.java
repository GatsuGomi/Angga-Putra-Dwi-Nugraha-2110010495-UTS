    public void AmbilData(){
        ArrayList<BkAlamat> datashow = new ArrayList<BkAlamat>();
        DefaultTableModel dt = (DefaultTableModel) TBALAMAT.getModel();
        
        datashow = arraybuku;
        Object[] row = new Object[5];
        for (int i = 0; i < datashow.size(); i++) {
            row[0] = datashow.get(i).getId();
            row[1] = datashow.get(i).getNama();
            row[2] = datashow.get(i).getJK();
            row[3] = datashow.get(i).getTelp();
            row[4] = datashow.get(i).getAlamat();
            dt.addRow(row);
        }
    }
    
    public final void TampilData(){
        DefaultTableModel dtm = (DefaultTableModel) TBALAMAT.getModel();
        dtm.setRowCount(0);
        AmbilData();
    }
    
